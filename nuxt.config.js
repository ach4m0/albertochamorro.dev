import path from 'path'
import fs from 'fs'

function getDynamicPaths(type) {
  return fs
    .readdirSync(path.resolve(__dirname, 'content', type))
    .filter((filename) => path.extname(filename) === '.md')
    .map((filename) => `/blog/${path.parse(filename).name}`)
}

